// Receiver
public class Drone {
    public void takeOff() {
        System.out.println("Drone is taking off");
    }

    public void moveLeft() {
        System.out.println("Drone is moving left");
    }

    public void moveRight() {
        System.out.println("Drone is moving right");
    }

    public void getClearShot() {
        System.out.println("Drone is getting a clear shot");
    }

    public void fireSnowball() {
        System.out.println("Drone is firing a snowball! Stay frosty.");
    }
}
